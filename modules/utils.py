def escape_html(text: str) -> str:
    """Escape text before injecting it into custom HTML blocks."""
    return html.escape(text or "", quote=True)


def safe_filename(name: str, fallback: str = "download") -> str:
    """Return a filesystem-safe filename while preserving readability."""
    cleaned = re.sub(r"[^A-Za-z0-9._-]+", "_", (name or fallback).strip()).strip("._")
    return cleaned[:80] or fallback


def _uploaded_bytes(uploaded) -> bytes:
    """Read bytes from a Streamlit UploadedFile without depending on pointer state."""
    if uploaded is None:
        return b""
    if hasattr(uploaded, "getvalue"):
        return uploaded.getvalue()
    return uploaded.read()


def extract_text_from_upload(uploaded) -> str:
    """Extract text from TXT, PDF, or DOCX uploads with graceful fallbacks."""
    if uploaded is None:
        return ""

    filename = getattr(uploaded, "name", "uploaded_file")
    ext = filename.rsplit(".", 1)[-1].lower() if "." in filename else ""
    raw = _uploaded_bytes(uploaded)

    if not raw:
        return ""

    if ext == "txt":
        return raw.decode("utf-8", errors="replace").strip()

    if ext == "pdf":
        try:
            import pypdf
            reader = pypdf.PdfReader(BytesIO(raw))
            return "\n\n".join(page.extract_text() or "" for page in reader.pages).strip()
        except ImportError:
            try:
                import pdfplumber
                with pdfplumber.open(BytesIO(raw)) as pdf:
                    return "\n\n".join(page.extract_text() or "" for page in pdf.pages).strip()
            except ImportError as exc:
                raise RuntimeError("PDF support requires `pypdf` or `pdfplumber`. Run `pip install pypdf`.") from exc

    if ext == "docx":
        try:
            import docx as _docx
            doc = _docx.Document(BytesIO(raw))
            return "\n".join(p.text for p in doc.paragraphs if p.text.strip()).strip()
        except ImportError:
            try:
                import docx2txt
                with tempfile.NamedTemporaryFile(suffix=".docx", delete=False) as tmp:
                    tmp.write(raw)
                    tmp_path = tmp.name
                try:
                    return (docx2txt.process(tmp_path) or "").strip()
                finally:
                    try:
                        os.remove(tmp_path)
                    except OSError:
                        pass
            except ImportError as exc:
                raise RuntimeError("DOCX support requires `python-docx` or `docx2txt`. Run `pip install python-docx`.") from exc

    raise RuntimeError(f"Unsupported file type: .{ext or 'unknown'}. Please upload TXT, PDF, or DOCX.")


def build_docx_bytes(title: str, body: str) -> bytes:
    """Create a simple DOCX file in memory for generated prompts."""
    try:
        import docx
    except ImportError as exc:
        raise RuntimeError("DOCX export requires `python-docx`. Install it with `pip install python-docx`.") from exc

    document = docx.Document()
    document.add_heading(title or "Generated Master Prompt", level=1)
    document.add_paragraph("Generated by Master Productivity Prompt Studio — Dr Alok Tiwari")
    document.add_paragraph("")
    for block in (body or "").split("\n"):
        document.add_paragraph(block)
    buffer = BytesIO()
    document.save(buffer)
    return buffer.getvalue()


# ─── Load / Save helpers ──────────────────────────────────────────────────────
def load_json(filepath: str, default=None):
    if default is None:
        default = []
    try:
        if os.path.exists(filepath):
            with open(filepath, "r", encoding="utf-8") as f:
                return json.load(f)
    except json.JSONDecodeError:
        # Keep the app usable even if a local JSON file is damaged.
        backup = f"{filepath}.corrupt_{datetime.now().strftime('%Y%m%d_%H%M%S')}"
        try:
            os.replace(filepath, backup)
        except OSError:
            pass
    except Exception:
        pass
    return default


def save_json(filepath: str, data):
    os.makedirs(os.path.dirname(filepath), exist_ok=True)
    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2, ensure_ascii=False)


def load_sample_inputs() -> dict:
    return load_json(SAMPLE_INPUTS_FILE, {})


def load_templates() -> dict:
    return load_json(TEMPLATES_FILE, {})


def load_saved_prompts() -> list:
    return load_json(SAVED_PROMPTS_FILE, [])


def save_prompts_to_disk(prompts: list):
    os.makedirs(DATA_DIR, exist_ok=True)
    save_json(SAVED_PROMPTS_FILE, prompts)


# ─── Prompt library helpers ───────────────────────────────────────────────────
def save_prompt_to_library(title: str, category: str, tags: list, prompt_text: str):
    """Append a new prompt entry to the library JSON."""
    prompts = load_saved_prompts()
    entry = {
        "id": str(uuid.uuid4()),
        "title": title,
        "category": category,
        "tags": tags,
        "prompt": prompt_text,
        "created_at": datetime.now().isoformat(),
        "updated_at": datetime.now().isoformat(),
    }
    prompts.append(entry)
    save_prompts_to_disk(prompts)
    return entry


def delete_prompt_from_library(prompt_id: str):
    prompts = load_saved_prompts()
    prompts = [p for p in prompts if p.get("id") != prompt_id]
    save_prompts_to_disk(prompts)


def update_prompt_in_library(prompt_id: str, updates: dict):
    prompts = load_saved_prompts()
    for p in prompts:
        if p.get("id") == prompt_id:
            p.update(updates)
            p["updated_at"] = datetime.now().isoformat()
    save_prompts_to_disk(prompts)


