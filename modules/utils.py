def template_upload_section(
    module_key: str,
    key_prefix: str,
    label: str = "Template",
    help_text: str = "Attach or select a template so the AI matches its exact structure and format.",
) -> str:
    """
    Render a collapsible template attachment section with:
    - Built-in named templates (dropdown)
    - File upload (PDF / DOCX / TXT)
    - Editable preview

    Returns the final template text (empty string if none provided).
    """
    templates = _BUILTIN_TEMPLATES.get(module_key, {})
    text_key  = f"{key_prefix}_template_text"

    with st.expander(f"📐 Attach or Select a {label} Template (optional)", expanded=False):
        st.markdown(
            f"""<div style="background:#F0F7FF;border-left:4px solid #2196F3;
            border-radius:6px;padding:10px 14px;margin-bottom:12px;font-size:0.85rem;
            color:#1A3A5C;">
            <b>ℹ️ Why attach a template?</b><br>
            {help_text} The generated prompt will instruct the AI to follow
            your template's structure, headings, and format exactly.
            </div>""",
            unsafe_allow_html=True,
        )

        # Source selector
        source = st.radio(
            "Template source:",
            ["None", "Built-in template", "Upload file", "Paste manually"],
            horizontal=True,
            key=f"{key_prefix}_tpl_source",
        )

        extracted = ""

        if source == "Built-in template" and templates:
            chosen = st.selectbox(
                "Select a built-in template:",
                list(templates.keys()),
                key=f"{key_prefix}_tpl_builtin",
            )
            extracted = templates[chosen]
            st.success(f"✅ Loaded: **{chosen}**")

        elif source == "Upload file":
            uploaded = st.file_uploader(
                "Upload template (PDF / DOCX / TXT)",
                type=["pdf", "docx", "txt"],
                key=f"{key_prefix}_tpl_file",
            )
            if uploaded:
                try:
                    extracted = extract_text_from_upload(uploaded)
                    if extracted:
                        st.success(f"✅ Extracted {len(extracted.split()):,} words from `{uploaded.name}`")
                    else:
                        st.warning("The file was readable but no text could be extracted. Paste the template manually below.")
                except Exception as e:
                    st.error(f"Could not read template: {e}")

        elif source == "Paste manually":
            extracted = ""  # let the text_area below capture it

        # Editable preview (always shown unless "None")
        if source != "None":
            current = extracted if extracted else st.session_state.get(text_key, "")
            template_text = st.text_area(
                "📋 Template content (editable):",
                value=current,
                height=220,
                key=text_key,
                placeholder="Your template structure will appear here. Edit as needed...",
            )
            # Show character count
            if template_text.strip():
                st.caption(f"{len(template_text.split()):,} words · {len(template_text):,} characters")
            return template_text
        else:
            # Clear stored template when "None" is selected
            if text_key in st.session_state:
                st.session_state[text_key] = ""
            return ""


def format_template_instruction(template_text: str, label: str = "output") -> str:
    """
    Convert template text into a prompt instruction block.
    Returns empty string if no template provided.
    """
    if not template_text or not template_text.strip():
        return ""
    return f"""
TEMPLATE TO FOLLOW:
The {label} must strictly follow the structure, headings, and format of the template below.
Do not add or remove major sections. Preserve all field labels and table structures.
Fill in every field with appropriate, substantive content.

--- TEMPLATE START ---
{template_text.strip()}
--- TEMPLATE END ---
"""


