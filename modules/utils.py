def studio_tabs(key_prefix: str, has_preview: bool = False):
    """
    Render a clean, single-row tab selector for each studio module.

    The selector is backed by a native Streamlit radio widget for accessibility
    and responsiveness. Programmatic navigation, such as jumping to Final
    Prompt after generation, is handled through a separate pending-tab key.
    This avoids Streamlit's rule against modifying a widget-backed
    ``st.session_state`` key after that widget has already been instantiated
    during the current run.
    """
    tab_key = f"{key_prefix}_active_tab"
    requested_key = f"{key_prefix}_requested_tab"
    options = ["input", "output"]
    labels = {"input": "Inputs", "preview": "Preview", "output": "Final Prompt"}

    if has_preview:
        options = ["input", "preview", "output"]

    # Apply any tab switch requested during the previous run before the radio
    # widget is created. This is the safe way to move users to the output tab
    # after they click Generate Prompt inside the input tab.
    requested = st.session_state.pop(requested_key, None)
    if requested in options:
        st.session_state[tab_key] = requested

    if st.session_state.get(tab_key) not in options:
        st.session_state[tab_key] = "input"

    active = st.radio(
        label=f"studio tab navigation {key_prefix}",
        options=options,
        index=options.index(st.session_state[tab_key]),
        format_func=lambda value: labels[value],
        horizontal=True,
        label_visibility="collapsed",
        key=tab_key,
        help="Switch between the input workspace and the generated output.",
    )
    return active


def request_studio_tab(key_prefix: str, target_tab: str):
    """Request a tab switch for the next rerun without touching widget state."""
    st.session_state[f"{key_prefix}_requested_tab"] = target_tab
    st.rerun()


def go_to_output(key_prefix: str):
    """Switch the studio tab to Final Prompt and trigger a rerun safely."""
    request_studio_tab(key_prefix, "output")


